class Switch 
{
	public static void main(String[] args) 
	{
		int day=3;
		switch(day){
   			case 1->System.out.println("tues");
			case 2->System.out.println("thursday");
			case 3->System.out.println("friday");
			case 4->System.out.println("saturday");
			default->System.out.println("weekend");
		}

	}
}
