class Months{
	public static void main(String[]args){
		String s="FEB";
		switch(s){
			case "JAN"->System.out.println(s+" is a 31 days of a month");
			case "FEB"->System.out.println(s+" is a 28 days of a month");
			case "MAR"->System.out.println(s+" is a 31 days of a month");
			case "APR"->System.out.println(s+" is a 30 days of a month");
			case "MAY"->System.out.println(s+" is a 31 days of a month");
			case "JUNE"->System.out.println(s+" is a 30 days of a month");
			case "JULY"->System.out.println(s+" is a 31 days of a month");
			case "AUG"->System.out.println(s+" is a 31 days of a month");
			case "SEP"->System.out.println(s+" is a 30 days of a month");
			case "OCT"->System.out.println(s+" is a 31 days of a month");
			case "NOV"->System.out.println(s+" is a 30 days of a month");
			case "DEC"->System.out.println(s+" is a 31 days of a month");
			default->System.out.println(s+" is not a month");
			}
	}
} 

			
		
			
		